def add_numbers(a, b):
    """
    Функция для сложения двух чисел.
    :param a: Первое число.
    :param b: Второе число.
    :return: Сумма чисел a и b.
    """
    return a + b

def multiply_numbers(a, b):
    """
    Функция для умножения двух чисел.
    :param a: Первое число.
    :param b: Второе число.
    :return: Результат умножения a на b.
    """
    return a * b